# Especificació de la tasca: 2. Suma de diferents parelles de nombres. Defineix la funció suma(a, b). Fes tres crides amb diferents valors: suma(2, 3), suma(10, 20), suma(-5, 7). Imprimeix el resultat de cada crida.

def suma(a, b):
    return a + b
resultat1 = suma(2, 3)
print(f"La suma de 2 i 3 és: {resultat1}")  
resultat2 = suma(10, 20)
print(f"La suma de 10 i 20 és: {resultat2}")    
resultat3 = suma(-5, 7)
print(f"La suma de -5 i 7 és: {resultat3}") 
